package com.ticketing.model;

public class BusHalt {
    private String busHaltId;
    private String busHaltName;
    private String busHaltLocation;

    public String getBusHaltId() {
        return busHaltId;
    }

    public void setBusHaltId(String busHaltId) {
        this.busHaltId = busHaltId;
    }

    public String getBusHaltName() {
        return busHaltName;
    }

    public void setBusHaltName(String busHaltName) {
        this.busHaltName = busHaltName;
    }

    public String getBusHaltLocation() {
        return busHaltLocation;
    }

    public void setBusHaltLocation(String busHaltLocation) {
        this.busHaltLocation = busHaltLocation;
    }
}
